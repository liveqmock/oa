package com.icss.oa.statsite.vo;

public class HourVO  {
	
	private String date;
	private Long count;
	private Long hour;
	
	public String getdate() {
		return date;
	}
	public void setdate(String _date) {
		date = _date;
	}
	public Long getcount() {
		return count;
	}
	public void setcount(Long _count) {
		count = _count;
	}
	public Long gethour() {
		return hour;
	}
	public void sethour(Long _hour) {
		hour = _hour;
	}
}